function [ y ] = lsquant( q,e,a,p,r,t,growth )
y(1) = ((q(1)/(a(1)*(1+growth)^t))^(1/e(1)) - p(1)) - ((q(2)/(a(2)*(1+growth)^t))^(1/e(2)) - p(2));
y(2) = ((q(2)/(a(2)*(1+growth)^t))^(1/e(2)) - p(2)) - ((q(3)/(a(3))^(1/e(3))-p(3)));
y(3) = ((q(3)/a(3))^(1/e(3)) - p(3)) - ((q(4)/a(4)).^(1/e(4))-p(4));
y(4) = q(1)+q(2)+q(3)+q(4) - r;
end




